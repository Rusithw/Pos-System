    public void getProducts() {
        dbcon.connect();
        dbcon.getData("SELECT * FROM product");
        try {
            while (dbcon.rs.next()) {
                String product_name = dbcon.rs.getString("product_name");
                String product_desc = dbcon.rs.getString("product_desc");
                String product_price = dbcon.rs.getString("product_price");
                String stock = dbcon.rs.getString("product_stock");
                String product_id = dbcon.rs.getString("product_id");

                String dataRow[] = {product_id, product_name, product_desc, product_price, stock};
                DefaultTableModel tb = (DefaultTableModel) product_list.getModel();
                tb.addRow(dataRow);

            }
        } catch (Exception e) {
            System.out.println(e);
        }
    }

