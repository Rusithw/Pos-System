    private void edit_buttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_edit_buttonActionPerformed

        int row = product_list.getSelectedRow();
        if (row < 0) {
            view_products_error.setText("Please Select a Product First..!!");
        } else {
            String id = product_list.getValueAt(row, 0).toString();
            String name = product_list.getValueAt(row, 1).toString();
            String desc = product_list.getValueAt(row, 2).toString();
            String price = product_list.getValueAt(row, 3).toString();
            String stock = product_list.getValueAt(row, 4).toString();

            EditProduct edit_product = new EditProduct(id, name, desc, price, stock);
            edit_product.setVisible(true);
            ViewProducts viewproducts_view = new ViewProducts();
            viewproducts_view.setVisible(false);
            dispose();
        }
    }//GEN-LAST:event_edit_buttonActionPerformed

