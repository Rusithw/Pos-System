    private void edit_buttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_edit_buttonActionPerformed
        int row = customer_list.getSelectedRow();
        if (row < 0) {
            view_customers_error.setText("Please Select a customer First..!!");
        } else {
            String id = customer_list.getValueAt(row, 0).toString();
            String name = customer_list.getValueAt(row, 1).toString();
            String phone = customer_list.getValueAt(row, 2).toString();
            String address = customer_list.getValueAt(row, 3).toString();

            EditCustomer edit_customer = new EditCustomer(id, name, phone, address);
            edit_customer.setVisible(true);
            ViewCustomers viewcustomers_view = new ViewCustomers();
            viewcustomers_view.setVisible(false);
            dispose();
        }
    }//GEN-LAST:event_edit_buttonActionPerformed

