    public void getCustomer(String text) {
        
        dbcon.connect();
        
        if (text.equals("")) {
            dbcon.getData("SELECT * FROM customer");
        } else {
            dbcon.getData("SELECT * FROM customer WHERE customer_name LIKE '%" + text + "%'");
        }
        DefaultTableModel tb = (DefaultTableModel) customer_list.getModel();

        if (tb.getRowCount() > 0) {
            for (int i = tb.getRowCount() - 1; i > -1; i--) {
                tb.removeRow(i);
            }
        }

        try {
            while (dbcon.rs.next()) {
                String customer_id = dbcon.rs.getString("customer_id");
                String customer_name = dbcon.rs.getString("customer_name");
                String phone_number = dbcon.rs.getString("customer_phone");
                String address = dbcon.rs.getString("customer_address");

                String dataRow[] = {customer_id, customer_name, phone_number, address};

                tb.addRow(dataRow);

            }
        } catch (Exception e) {
            System.out.println(e);
        }
    }

