    public void getSelect_Customers() {
        dbcon.connect();
        dbcon.getData("SELECT * FROM customer");
        select_customer.removeAllItems();
        try {
            while (dbcon.rs.next()) {
                String customer_id = dbcon.rs.getString("customer_id");
                String customer_name = dbcon.rs.getString("customer_name");
                select_customer.addItem(customer_name);
                customer_list.add(customer_id);
            }
        } catch (Exception e) {
            System.out.println(e);
        }

        dbcon.disconnect();
    }

