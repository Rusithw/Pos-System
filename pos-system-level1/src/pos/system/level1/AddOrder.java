    private void addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addActionPerformed
        int selected_index = select_product.getSelectedIndex();
        DefaultTableModel tb = (DefaultTableModel) products.getModel();
        String selected_product_id = product_list.get(selected_index);

        dbcon.connect();
        dbcon.getData("SELECT * FROM product WHERE product_id='" + selected_product_id + "'");
        try {
            if (dbcon.rs.next()) {
                String product_id = dbcon.rs.getString("product_id");
                String product_name = dbcon.rs.getString("product_name");
                String product_desc = dbcon.rs.getString("product_desc");
                String product_price = dbcon.rs.getString("product_price");

                String[] dataset = {product_id, product_name, product_desc, quantity.getText(), product_price};
                tb.addRow(dataset);

                total = (Double.parseDouble(product_price) * Double.parseDouble(quantity.getText())) + total;
                total_price.setText(total + "");
            }
        } catch (Exception e) {
            System.out.println(e);
        }

        dbcon.disconnect();

    }//GEN-LAST:event_addActionPerformed

